package com.jyuesong.android.schedule;

/**
 * 订阅者的基类，这里的{@getNotify()} 和 {@getError()} 只会执行其中一个方法
 * Created by jiang on 5/29/16.
 */
public interface Observer<T> {

    /**
     * 通过该方法获取数据
     *
     * @param t
     */
    void notifyData(T t);

    void error(Throwable t);

    void onStart();

    void onAfter();
}
